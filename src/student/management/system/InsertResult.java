    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        String course = (String)jComboBox1.getSelectedItem();
        String rollno = jTextField1.getText();
        String dbtable=""; String rtable="";
        if(course=="B.Tech"){ dbtable="btstudent"; rtable="btresult";}
        else if(course=="M.Tech") { dbtable="mtstudent"; rtable="mtresult";}
        else { dbtable="mbastudent"; rtable="mbaresult";}
        
         try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/utkarsh","root","Utkarsh@1");
            
            Statement st = con.createStatement();
            ResultSet rs=st.executeQuery("Select * from "+dbtable+" where rollno='"+rollno+"' ");
            if(rs.next()){
                if(rtable=="btresult"){
                    Subject1.setText("Physics");
                    Subject2.setText("Chemistry");
                    Subject3.setText("Mathematics");
                    Subject4.setText("Electronics");
                    Subject5.setText("DBMS");
                }
                else if(rtable=="mtresult"){
                    Subject1.setText("Computer Networks");
                    Subject2.setText("Advance OS");
                    Subject3.setText("Advance DBMS");
                    Subject4.setText("Information Security");
                    Subject5.setText("Cloud Computing");
                }
                else{
                    Subject1.setText("Research Methods");
                    Subject2.setText("Economics");
                    Subject3.setText("Marketing Management");
                    Subject4.setText("Finance");
                    Subject5.setText("Accounting");
                }
                Subject1.setOpaque(true);Subject2.setOpaque(true);Subject3.setOpaque(true);Subject4.setOpaque(true);Subject5.setOpaque(true);
                jTextField2.setVisible(true);
                jTextField3.setVisible(true);
                jTextField4.setVisible(true);
                jTextField5.setVisible(true);
                jTextField6.setVisible(true);
                jButton6.setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null,"No student found");
                setVisible(false);
                new InsertResult().setVisible(true);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
  
    }//GEN-LAST:event_jButton7ActionPerformed

