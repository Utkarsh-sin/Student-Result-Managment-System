    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String course = (String)jComboBox1.getSelectedItem();
        String rollno = jTextField1.getText();
        String subject1 = jTextField2.getText();
        String subject2 = jTextField3.getText();
        String subject3 = jTextField4.getText();
        String subject4 = jTextField5.getText();
        String subject5 = jTextField6.getText();
        String dbtable=""; String rtable="";
        if(course=="B.Tech"){ dbtable="btstudent"; rtable="btresult";}
        else if(course=="M.Tech") { dbtable="mtstudent"; rtable="mtresult";}
        else { dbtable="mbastudent"; rtable="mbaresult";}
        
         try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/utkarsh","root","Utkarsh@1");
            
            Statement st = con.createStatement();
            ResultSet rs=st.executeQuery("Select * from "+dbtable+" where rollno='"+rollno+"' ");
            if(rs.next() && !subject1.isEmpty() && !subject2.isEmpty() && !subject3.isEmpty() && !subject4.isEmpty() && !subject5.isEmpty()){
                if(rtable=="btresult"){
                   st.executeUpdate("insert into "+rtable+" values('"+rollno+"','"+subject1+"','"+subject2+"','"+subject3+"','"+subject4+"','"+subject5+"')");
                   JOptionPane.showMessageDialog(null,"Result added successfully");
                }
                else if(rtable=="mtresult"){
                   st.executeUpdate("insert into "+rtable+" values('"+rollno+"','"+subject1+"','"+subject2+"','"+subject3+"','"+subject4+"','"+subject5+"')");
                   JOptionPane.showMessageDialog(null,"Result added successfully");
                }
                else if(rtable=="mbaresult"){
                   st.executeUpdate("insert into "+rtable+" values('"+rollno+"','"+subject1+"','"+subject2+"','"+subject3+"','"+subject4+"','"+subject5+"')");
                   JOptionPane.showMessageDialog(null,"Result added successfully");
                } 
            }
            else{
                JOptionPane.showMessageDialog(null,"Please add all subjects marks");
                setVisible(false);
                new InsertResult().setVisible(true);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
        
    }//GEN-LAST:event_jButton6ActionPerformed

