    public void setData(String rollno,String course){
        String rtable="";
        String dbtable="";
        jTextField2.setText(rollno);
        jTextField3.setText(course);
        if(course=="B.Tech"){ dbtable="btstudent"; rtable="btresult";}
        else if(course=="M.Tech") { dbtable="mtstudent"; rtable="mtresult";}
        else { dbtable="mbastudent"; rtable="mbaresult";}
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/utkarsh","root","Utkarsh@1");
            
            Statement st = con.createStatement();  
            ResultSet rs=st.executeQuery("Select * from "+dbtable+" where rollno='"+rollno+"'");
            
            if(rs.next()){
                jTextField1.setText(rs.getString("name"));
                jTextField4.setText(rs.getString("branch"));
                jTextField5.setText(rs.getString("gender"));
                jTextField6.setText(rs.getString("fathername"));
                
            }
        }
            catch(Exception e){
                JOptionPane.showMessageDialog(null,e.toString());
            }
            try{
                Class.forName("com.mysql.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/utkarsh","root","Utkarsh@1");
            
                Statement st1 = con.createStatement();
                ResultSet rs1=st1.executeQuery("Select * from "+rtable+" where rollno='"+rollno+"'");
            if(rs1.next()){
            if(rtable=="btresult"){
                    subject1.setText("Physics");
                    subject2.setText("Chemistry");
                    subject3.setText("Mathematics");
                    subject4.setText("Electronics");
                    subject5.setText("DBMS");                 
                }
                else if(rtable=="mtresult"){
                    subject1.setText("Computer Networks");
                    subject2.setText("Advance OS");
                    subject3.setText("Advance DBMS");
                    subject4.setText("Information Security");
                    subject5.setText("Cloud Computing");
                }
                else{
                    subject1.setText("Research Methods");
                    subject2.setText("Economics");
                    subject3.setText("Marketing Management");
                    subject4.setText("Finance");
                    subject5.setText("Accounting");
                }
                String marks1=rs1.getString(2);
                String marks2=rs1.getString(3);
                String marks3=rs1.getString(4);
                String marks4=rs1.getString(5);
                String marks5=rs1.getString(6);
                jTextField17.setText(marks1);
                jTextField18.setText(marks2);
                jTextField19.setText(marks3);
                jTextField20.setText(marks4);
                jTextField21.setText(marks5);
                int total =(Integer.valueOf(marks1)+Integer.valueOf(marks2)+Integer.valueOf(marks3)+Integer.valueOf(marks4)+Integer.valueOf(marks5));
                jTextField22.setText(String.valueOf(total));
                if(total>=200){
                    jLabel16.setText("Passed!");
                }
                else jLabel16.setText("Failed!");
            }
            }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }
